    private void btnConsultarPersonasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarPersonasActionPerformed
        javax.swing.table.DefaultTableModel model
                = (javax.swing.table.DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        // Obtenemos las personas ordenadas desde el controlador
        java.util.List<Person> persons = personController.getPersonsOrderedById();

        for (Person person : persons) {

            String tipo;
            String editorial;
            int cantLibros;

            switch (person) {
                case Author a -> {
                    tipo = "Autor";
                    editorial = "-";
                    cantLibros = a.getBookQuantity();
                }
                case Manager m -> {
                    tipo = "Gerente";
                    editorial = (m.getPublisher() != null) ? m.getPublisher().getName() : "-";
                    cantLibros = 0;
                }
                case Narrator n -> {
                    tipo = "Narrador";
                    editorial = "-";
                    cantLibros = n.getBookQuantity();
                }
                default -> {
                    continue; 
                }
            }

            model.addRow(new Object[]{
                person.getId(),
                person.getFullname(),
                tipo,
                editorial,
                cantLibros
            });
        }
    }//GEN-LAST:event_btnConsultarPersonasActionPerformed

